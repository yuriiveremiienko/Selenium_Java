package homework_5;


public class Animal {

    int height;
    int length;
    String color;
    String sound;
    boolean canSwim;

    public Animal(int height, int length,  String color, String sound, boolean canSwim) {

        this.height = height;
        this.length = length;
        this.color = color;
        this.sound = sound;
        this.canSwim = canSwim;
    }
}
